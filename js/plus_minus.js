

       function plus(valeur){
        valeur=parseInt(valeur);
        if(valeur >=0 ){
            valeur+=1;
        }
        else{
            valeur=1
        }
        return valeur;
    }

    
    function minus(valeur){
        valeur=parseInt(valeur);
        if(valeur >0 ){
            valeur-=1;
        }
        else{
            valeur=0
        }
        return valeur;
    }